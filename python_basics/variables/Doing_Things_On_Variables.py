# Math with Numbers

current_salary = 500000
target_salary = 1500000
increase_needed = target_salary - current_salary
print(increase_needed) #1000000

#Joining Strings - called concatenation

first_name = "Prapti"
last_name = "Sinha"
full_name = first_name + " " + last_name
print(full_name)
# Prapti Sinha

#f-string - the clean way to mix variables and text

months_to_goal = 8
print(f"I will become an AI Engineer in {months_to_goal} months")
# I will become an AI Engineer in 8 months

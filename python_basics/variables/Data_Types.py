#Data Types
# 1. String - text, always in quotes
city = "Patna"
role = "AI Engineer"

# 2. Integer - whole numbers no quotes
years_experience = 4
target_salary = 1500000

# 3. Float - numbers with decimal
 
gpa= 7.8
tax_rate = 0.30

# 4. Boolean - only true or false

has_job = True
knows_python = False

# check what type any variable is 
print(type(city))      #<class 'str'>
print(type(role))
print(type(years_experience))    #<class 'int'>
print(type(target_salary))
print(type(gpa))
print(type(tax_rate))
print(type(has_job))
print(type(knows_python))